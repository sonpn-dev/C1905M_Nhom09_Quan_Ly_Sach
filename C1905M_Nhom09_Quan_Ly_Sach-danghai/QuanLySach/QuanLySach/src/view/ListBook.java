    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblSach.getSelectedRow();
        int id=-1;
        int count = 0;
       
        for (Book str : list) {
            if(count == row){
                id = str.getId();
            }
            count++;
        }
        if(id == -1){
            JOptionPane.showMessageDialog(rootPane,"Vui lòng chọn dòng cần xóa");
        }else if(list.size() ==0){
            JOptionPane.showMessageDialog(rootPane,"Không có dữ liệu để xóa");
        }else{
            boolean ok = new DAO().removeBook(id);
            
            if(ok){
                System.out.println("1");
            }else{
                
               list = new DAO().getListBook();
                model.setRowCount(0);
                model = (DefaultTableModel) tblSach.getModel();

                model.setColumnIdentifiers(new Object[]{
                    "STT","Tên sách","Danh mục sách","Tên tác giả","Nhà XB","Năm XB"
                });
                showTable();
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblSach.getSelectedRow();
        int id=-1;
        int count = 0;
        for (Book str : list) {
            if(count == row){
                id = str.getId();
            }
            count++;
        }
        if(id == -1){
            JOptionPane.showMessageDialog(rootPane,"Vui lòng chọn dòng cần cập nhật");
        }else if(list.size() ==0){
            JOptionPane.showMessageDialog(rootPane,"Không có dữ liệu để cập nhật");
        }else{
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
        
            EditBook editBook = new EditBook(frame,true,list,row);
            editBook.setVisible(true);
            list = new DAO().getListBook();
            model.setRowCount(0);
            model = (DefaultTableModel) tblSach.getModel();

            model.setColumnIdentifiers(new Object[]{
                "STT","Tên sách","Danh mục sách","Tên tác giả","Nhà XB","Năm XB"
            });
            showTable();
        }
        
    }//GEN-LAST:event_btnEditActionPerformed


